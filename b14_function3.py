def A():
    print("abs: ", abs(-20))
    print("len: ", len([1, 2, 3]))
    print("max: ", max([1, 2, 3]))
    print("min: ", min([1, 2, 3]))
    print("sum: ", sum([1, 2, 3], 4))
    print("pow: ", pow(4, 3))
    print("round: ", round(10.5))
    print("divmod: ", divmod(5, 2))


def B():
    print("chr: ", chr(65))
    print("ord: ", ord('A'))
    print("bin: ", bin(4))
    print("oct: ", oct(8))
    print("hex: ", hex(16))
    print("complex: ", complex(1, 2))
    print("int: ", int("123"))
    print("str: ", str(123))
    print("float: ", float("3.1416"))
    print("bool: ", bool(0))


A()
B()
